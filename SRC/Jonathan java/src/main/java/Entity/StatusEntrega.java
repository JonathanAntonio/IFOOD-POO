package Entity;


public class StatusEntrega {

    private int id;

    private String nome;

    public StatusEntrega(int i) {
    }

    public StatusEntrega(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
